class Solution
{
    int ret;

public:
    int goodNodes(TreeNode *root)
    {
        ret = 0;
        dfs(root, INT_MIN);
        return ret;
    }
    void dfs(TreeNode *node, int M)
    {
        if (node == NULL)
            return;
        if (node->val >= M) {
            ret++;
        }
        M = max(node->val, M);
        dfs(node->right, M);
        dfs(node->left, M);
    }
};

