class Solution
{
public:
    int jump(vector<int> &nums)
    {
        vector<int> dp(nums.size(), INT_MAX);
        // dp[i] min step from 0 to i.
        int n = nums.size();
        dp[0] = 0;
        for (int i = 0; i < n - 1; i++) {
            for (int j = i + 1; j < n && j <= i + nums[i]; j++) {
                dp[j] = min(dp[i] + 1, dp[j]);
            }
        }
        return dp[n - 1];
    }
};

