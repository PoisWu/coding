class Solution
{
public:
    int maxFrequencyElements(vector<int> &nums)
    {
        int freq[101] = {0};
        for (int x : nums) {
            freq[x]++;
        }
        int max_freq = 0;
        int res = 0;
        for (int i = 0; i < 101; i++) {
            if (freq[i] > max_freq) {
                max_freq = freq[i];
                res = 1;
            } else if (freq[i] == max_freq) {
                res++;
            }
        }
        return res * max_freq;
    }
};

