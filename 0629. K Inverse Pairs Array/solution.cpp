class Solution
{
private:
    static const int mod = 1000000007;

public:
    int kInversePairs(int n, int k)
    {
        // 0 - k pair permutations
        vector<vector<int>> dp(n + 1, vector<int>(k + 1, 0));
        for (int i = 0; i <= n; i++) {
            dp[i][0] = 1;
        }
        for (int i = 1; i <= n; i++) {
            for (int j = 0; j <= k; j++) {
                dp[i][j] = (dp[i - 1][j] + (j > 0 ? dp[i][j - 1] : 0)) % mod;
                dp[i][j] =
                    (dp[i][j] + mod - (i >= j ? dp[i - 1][j - i] : 0)) % mod;
            }
        }
        return dp[n][k];
    }
};

